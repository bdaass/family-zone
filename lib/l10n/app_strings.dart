  static String of(String key) {
    final lang = LocaleService.instance.languageCode;
    return _map[lang]?[key] ?? _map['en']![key] ?? key;
  }

  static String fmt(String key, Map<String, String> params) {
    var text = of(key);
    for (final entry in params.entries) {
      text = text.replaceAll('{${entry.key}}', entry.value);
    }
    return text;
  }

  static String roleLabel(String role) {
    switch (role) {
      case 'admin':
        return of('role_admin');
      case 'employee':
        return of('role_employee');
      case 'client':
        return of('role_client');
      default:
        return role;
    }
  }

  /// Known product color names (stored in English) shown in the user's language.
  static String colorName(String raw) {
    final trimmed = raw.trim();
    if (trimmed.isEmpty) return trimmed;
    final key = _colorKeys[trimmed.toLowerCase()];
    if (key != null) return of(key);
    return trimmed;
  }

